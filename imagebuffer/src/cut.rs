pub fn cut(input: &str, start: usize, end: usize) -> &str {
    if start >= input.len() || end > input.len() || start >= end {
        ""
    } else {
        &input[start..end]
    }
}

fn main() {
    let text = "Hello, World!";
    let result = cut(text, 7, 12);
    println!("{}", result); // 输出: World
}

